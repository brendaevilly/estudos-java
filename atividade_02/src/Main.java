
public class Main {

	public static void main(String[] args) {
		int number1 = 10, number2 = 3;
		double result = 0;
		
		//Esse (double) é o Casting, a conversão explícita dos valores.
		//Nesse caso, conversão do resultado inteiro para double.
		result = (double) number1 / number2;
		
		System.out.println(result);
		
		double a = 5.5;
		int b = (int) a;
		
		System.out.println(b);

	}

}
